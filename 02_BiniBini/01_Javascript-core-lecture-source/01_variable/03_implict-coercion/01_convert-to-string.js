// [JavaScript - 문자열 타입으로의 암묵적 변환]

// JavaScript는 상황에 따라 자동으로 타입을 변환(암묵적 변환)한다.
// 여기서는 '문자열' 타입으로 자동 변환되는 경우를 알아본다.

// 1. 문자열 연결 연산자 (+)
// + 연산자는 하나라도 문자열이면 다른 피연산자도 '문자열'로 변환해서 연결한다.

console.log(10 + '20'); 
// 출력: '1020'
// 숫자 10이 문자열 '10'으로 변환되어
// '10' + '20' → '1020' 이 된다.

// 2. 템플릿 리터럴 (Template Literal)
// 백틱(``)을 사용한 문자열 안에 ${표현식}을 넣으면,
// 표현식 결과를 문자열로 변환해서 삽입한다.

console.log(`10 + 20 = ${10 + 20}`);
// 출력: '10 + 20 = 30'
// 10 + 20 계산 결과 30이 문자열 '30'으로 변환되어 삽입된다.
