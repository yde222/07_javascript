// [JavaScript 불린(Boolean) 타입]

// 1. 변수 선언과 초기화
// true(참) 또는 false(거짓)만 저장할 수 있는 타입을 Boolean(불린)이라고 한다.

var flag = true; // flag라는 변수에 true 값을 저장

// 2. 출력
console.log(`flag`, flag);  
// 출력: flag true
// (console.log 안에서 문자열과 변수 둘 다 출력)

// 3. 값 변경
flag = false; // flag 변수의 값을 false로 변경

// 4. 다시 출력
console.log(`flag`, flag);
// 출력: flag false
