// [JavaScript - 객체(Object) 키(Key) 작성 규칙 및 특징]

// 1. 객체 생성
var obj = {
    normal: 'normal value',          // 일반 키: 정상적으로 작성
    '@ s p a c e @' : 'space value', // 특수문자, 공백 포함 키: 가능 (하지만 따옴표로 감싸야 함)
    0 : 1,                           // 숫자 키: 내부적으로 문자열('0')로 변환되어 저장
    var : 'var',                     // 예약어(var 등) 사용: 문법 오류는 발생하지 않지만, 혼란을 줄 수 있어 권장하지 않음
    normal: 'new value',             // 중복된 키: 나중에 작성된 값으로 덮어쓰기 발생
};

// 2. 주의사항 및 특징 요약
// - 키 이름은 기본적으로 문자열(string)이다.
// - 공백이나 특수문자가 포함된 키는 반드시 따옴표로 묶어야 한다.
// - 숫자 키도 자동으로 문자열로 변환된다.
// - 같은 키를 여러 번 작성하면, 마지막에 작성된 값으로 덮어쓴다.
// - 예약어를 키로 사용해도 오류는 없지만 코드 가독성이 떨어지므로 피하는 것이 좋다.

// 3. 객체 확인
console.log(obj);
/*
출력 결과:
{
  '0': 1,
  normal: 'new value',
  '@ s p a c e @': 'space value',
  var: 'var'
}
*/
