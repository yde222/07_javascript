// 특정 자리수에서 내림(floor)
const positiveNum = 93.54;
const negativeNum = -39.27;

// 소수점 위치값 기준에서 내림
console.log(Math.floor(positiveNum));
console.log(Math.floor(negativeNum));
console.log(Math.floor(positiveNum * 10) / 10);
console.log(Math.floor(positiveNum / 10) * 10);
console.log(Math.floor(negativeNum * 10) / 10);
console.log(Math.floor(negativeNum / 10) * 10);






