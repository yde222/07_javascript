// 023. 논리 연산자 (Logical Operators)
// 논리 연산자는 여러 조건을 결합하여 true 또는 false를 반환합니다.
// AND, OR, NOT 연산자가 있습니다.

// AND 논리 연산자는 두 개의 조건이 모두 true일 때만 true를 반환합니다.
// OR 논리 연산자는 두 개의 조건 중 하나라도 true일 때 true를 반환합니다.
// NOT 논리 연산자는 조건의 true/false를 반전시킵니다.

console.log(true && true); // true
console.log(true && false); // false
console.log('문장' == '문장' && 5 == 5);    // true (문장 == 문장 이고 5 == 5 이므로 true)
console.log(5 == 5 && '다른 문장 1' == '다른 문장 2'); // true (5 == 5 이고 다른 문장 1 == 다른 문장 2 이므로 false)

// OR 논리 연산자 ||
console.log(true || false); 
console.log(false || false);
console.log('문장' == '문장' && '다른 문장 1' == '다른 문장 2');

// NOT 논리 연산자 !
